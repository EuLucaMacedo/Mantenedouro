    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        int linhaSelecionada = jTableAnimal.getSelectedRow();

        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um Item para Excluir!");
            return;
        }

        int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir esse registro?");
        if (resposta == JOptionPane.YES_OPTION) {
            
            int id = Integer.parseInt(jTableAnimal.getValueAt(linhaSelecionada, 0).toString());

            boolean sucesso = AnimalDao.excluir(id);
            if (sucesso) {
                JOptionPane.showMessageDialog(null, "Registro exclu√≠do com sucesso!");
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao excluir o registro.");
            }

            jTableAnimal.setModel(montarTabela());
            jScrollPane1.setViewportView(jTableAnimal);
        }

    }//GEN-LAST:event_jButtonExcluirActionPerformed

