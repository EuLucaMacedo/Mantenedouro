    private DefaultTableModel montarTabela(){
        AnimalDao dao = new AnimalDao();
        String [] colunas = {"Nome" , "Especie", "Data", "Responsavel", "Receituario"};
        
        DefaultTableModel tabela = new DefaultTableModel(colunas, 0);
        
        ArrayList<Animal> listar = (ArrayList<Animal>) AnimalDao.listarTodos();
        for(int i = 0; i < listar.size(); i++) {
            Animal a = listar.get(i);
            
            String[] linhas = {
                String.valueOf(a.getId()),
                a.getNome(),
                a.getEspecie(),
                a.getData(),
                a.getResponsavel(),
                a.getReceita(),
               
            };
            
            tabela.addRow(linhas);
            
        } 
        
        return tabela;
    }
    
