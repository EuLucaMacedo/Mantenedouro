    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        Animal a = new Animal();
        
        a.setNome(jTextNome.getText());
        a.setEspecie(jTextEspecie.getText());
        a.setData(jTextData.getText());
        a.setResponsavel(jTextProfissional.getText());
        a.setReceita(jTextReceituario.getText());
        
        AnimalDao dao = new AnimalDao();
        if(dao.cadastrar(a)){
            JOptionPane.showMessageDialog(null, "Animal cadastrado com sucesso!");
            jTextNome.setText("");
            jTextEspecie.setText("");
            jTextData.setText("");
            jTextProfissional.setText("");
            jTextReceituario.setText("");
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

